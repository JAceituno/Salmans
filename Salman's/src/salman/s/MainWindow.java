    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int peso = Integer.parseInt(jsp_peso.getValue().toString());
        int from = jc_from.getSelectedIndex();
        int to = jc_to.getSelectedIndex();
        boolean dirigido = isDrigida.isSelected();
        
        NumericalProperty wp = grafo.getEdgeWidthProperty();
        wp.setValue(to, peso);
        grafo.addSimpleEdge(from, to, dirigido);
        grafo.setEdgesWidth(wp);
    }//GEN-LAST:event_jButton2ActionPerformed


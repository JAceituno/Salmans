            double cantidad = Double.parseDouble(jsp_cantidadPlato.getValue().toString());
            String tipo = jc_tipoMedidaPlato.getSelectedItem().toString();
            String unidad = jc_unidadMedidaPlato.getSelectedItem().toString();

            Ingredient temp = new Ingredient(nombre,cantidad,unidad,tipo);

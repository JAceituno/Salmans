    private void cmd_agregarAlmacenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_agregarAlmacenActionPerformed
        if(tf_nombreIngrediente != null){
            int selec = JOptionPane.showConfirmDialog(jd_ingredientes, "¿Está seguro que desea agregar este ingrediente? Recuerde que no puede modificarse.","Confirmar",JOptionPane.YES_NO_OPTION);
            if(selec == JOptionPane.OK_OPTION){
                String nombre = tf_nombreIngrediente.getText();
                double cantidad = Double.parseDouble(jsp_cantidad.getValue().toString());
                String tipo = jc_tipoMedida.getSelectedItem().toString();
                String unidad = jc_unidadMedida.getSelectedItem().toString();
                
                Ingredient temp = new Ingredient(nombre,cantidad,unidad,tipo);
                storage.addToStorage(temp);
                
                DefaultComboBoxModel modelo = new DefaultComboBoxModel();
                for (int i = 0; i < storage.getAlmacen().size(); i++) {
                    modelo.addElement(storage.getAlmacen().get(i).peek().getName());
                }
                jc_cargarExistente.setModel(modelo);
                jc_eliminarIngrediente.setModel(modelo);
                
                tf_nombreIngrediente.setText("");
                
                JOptionPane.showMessageDialog(jd_ingredientes, "¡Ingrediente agregado con éxito!", "¡Bien hecho!", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(jd_ingredientes, "Nombre vacío", "Campo faltante", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cmd_agregarAlmacenActionPerformed

